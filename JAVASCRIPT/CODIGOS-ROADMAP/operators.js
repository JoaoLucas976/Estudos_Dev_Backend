// Aritmeticos
5 + 2  // Soma
5 - 2  // Subtração
5 * 2  // Multiplicação
5 / 2  // Divisão
5 % 2  // Resto da Divisão
5 ** 2 // Exponenciação

// Com variáveis
let n1 = 10
n1 += 10   // n1 = n1 + 10
n1 -= 10   // n1 = n1 - 10
n1 *= 10   // n1 = n1 * 10
n1 /= 10   // n1 = n1 / 10
n1 %= 10   // n1 = n1 % 10
n1 **= 10  // n1 = n1 ** 10

// Com variáveis
let n2 = 10
n2++ // Adição unitária
n2-- // Subtração unitária

// Operadores relacionais
5 > 2  // true
7 < 4  // false
8 >= 8 // true
9 <= 7 // false
5 == 5 // true
4 != 4 // false

// Identidade
5 == '5'  // true
5 === '5' // false
5 != '5'  // false
5 !== '5' // true

// Lógicos
let x = 10
let y = 20
!x == 10         // not true (false)
x != y && y > x  // true AND true (true)
x != y || y > x  // true OR true (true)

// Operador Ternário
let teste = y > x ? 'verdadeiro' : 'falso' // x é maior que y ? Se sim "verdadeiro", se não "falso"